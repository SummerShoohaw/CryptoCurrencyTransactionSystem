    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkDirectory().getNetworkDirectory()) {
            for (AbstractEnterprise enterprise: network.getEnterpriseDir().getEnterpriseDir()) {
                for (Account userAccount : enterprise.getAccountDir().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise;
                    row[1] = network;
                    row[2] = userAccount;

                    model.addRow(row);
                }
            }
        }
    }

    private void populateNetworkComboBox(){
        networkJComboBox.removeAllItems();
        
        for (Network network : system.getNetworkDirectory().getNetworkDirectory()){
            networkJComboBox.addItem(network);
        }
    }
    
    private void populateEnterpriseComboBox(Network network){
        enterpriseJComboBox.removeAllItems();
        
        for (AbstractEnterprise enterprise : network.getEnterpriseDir().getEnterpriseDir()){
            enterpriseJComboBox.addItem(enterprise);
        }
        
    }
    
