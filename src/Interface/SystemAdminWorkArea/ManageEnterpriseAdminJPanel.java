    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        userProcessContainer.remove(this);
         Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[0];
        JTreePanel jtreePanel = (JTreePanel) component;
        jtreePanel.populateTree();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.removeAll();
        userProcessContainer.add(jtreePanel);
        layout.next(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = enterpriseJTable.getSelectedRow();
        AbstractEnterprise enterprise  = (AbstractEnterprise) enterpriseJTable.getValueAt(row, 0);
        Account a = (Account) enterpriseJTable.getValueAt(row, 2);
        enterprise.getAccountDir().getUserAccountList().remove(a);
        EcoSystem.getEcoSystem().getAllUser().remove(a);
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

