    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        Network network = (Network) networkJComboBox.getSelectedItem();
        AbstractEnterprise.Type type = (AbstractEnterprise.Type) enterpriseTypeJComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String name = nameJTextField.getText();
        for(AbstractEnterprise e: network.getEnterpriseDir().getEnterpriseDir()){
            if(e.getName().equals(name)){
                JOptionPane.showMessageDialog(null, "This name has been registered!");
                return;
            }
            if(e.getType().equals(type) && (type.getValue().equals("Exchange") || type.getValue().equals("CoinEnterprise"))){
                JOptionPane.showMessageDialog(null, type.getValue()+" has exist !!");
                return;
            }
        }
        AbstractEnterprise enterprise = network.getEnterpriseDir().addEnterprise(name, type);
        if(type.getValue().equals("CoinEnterprise")){
            network.setCoinType(name);
            CoinEnterprise coinEnt = (CoinEnterprise)enterprise;
            coinEnt.setCoinType(name);
            coinEnt.setName(name);
        }
        populateTable();

    }//GEN-LAST:event_submitJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        try{
            Component component = componentArray[0];
        JTreePanel jtreePanel = (JTreePanel) component;
        jtreePanel.populateTree();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        userProcessContainer.removeAll();
        userProcessContainer.add(jtreePanel);
        layout.next(userProcessContainer);
        }catch(Exception e){
            System.out.println("No page");
        }
        

        
    }//GEN-LAST:event_backJButtonActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = enterpriseJTable.getSelectedRow();
        Network n = (Network) enterpriseJTable.getValueAt(row, 1);
        AbstractEnterprise e = (AbstractEnterprise) enterpriseJTable.getValueAt(row, 0);
        n.getEnterpriseDir().getEnterpriseDir().remove(e);
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

