    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkDirectory().getNetworkDirectory()) {
            for (AbstractEnterprise enterprise: network.getEnterpriseDir().getEnterpriseDir()) {
                Object[] row = new Object[3];
                row[0] = enterprise;
                row[1] = network;
                row[2] = enterprise.getType().getValue();

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        networkJComboBox.removeAllItems();
        enterpriseTypeJComboBox.removeAllItems();

        for (Network network : system.getNetworkDirectory().getNetworkDirectory()) {
            networkJComboBox.addItem(network);
        }

        for (AbstractEnterprise.Type type : AbstractEnterprise.Type.values()) {
            enterpriseTypeJComboBox.addItem(type);
        }

    }

